let _lang = localStorage.getItem('cago_lang') || 'es';
export const getLang = () => _lang;
export const setLang = lang => { _lang = lang; localStorage.setItem('cago_lang', lang); };
export const t = (key, vars) => {
  const str = (_lang === 'en' ? _en[key] : null) ?? _es[key] ?? key;
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => vars[k] ?? '');
};

